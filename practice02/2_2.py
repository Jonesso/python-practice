def f22(num):
    a = (num & 0x1)
    b = (num & ((0x1 << 16) - (0x1 << 1))) >> 1
    c = (num & ((0x1 << 23) - (0x1 << 16))) >> 16
    d = (num & ((0x1 << 28) - (0x1 << 23))) >> 23
    e = (num & ((0x1 << 31) - (0x1 << 28))) >> 28
    f = (num & (0x1 << 31)) >> 31
    
    return ((b << 17) | (a << 16) | (f << 15) | (c << 8) | (e << 5) | d)


